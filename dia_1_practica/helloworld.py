# Escriba un ejemplo para diferentes tipos de datos de Python, 
# como Número (Entero, Flotante, Complejo), 
# Cadena, Booleano, Lista, Tupla, Conjunto y Diccionario.
print(type(10))                                  # Entero
print(type(9.8))                                 # Flotante
print(type(3.14))                                # Flotante
print(type(4 - 4j))                              # Complejo
print(type("Ignacio"))                           # Cadena
print(type(True))                                # Booleano
print(type([1, 2, 3]))                           # Lista
print(type((1, 2, 3)))                           # Tupla (Se usan paréntesis)
print(type({1, 2, 3}))                           # Conjunto (Se usan llaves)
print(type({'name':'Ignacio'}))                  # Diccionario (Clave:Valor)
